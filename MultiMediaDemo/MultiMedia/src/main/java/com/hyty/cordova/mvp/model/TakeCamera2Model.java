@ActivityScope
public class TakeCamera2Model extends BaseModel implements TakeCamera2Contract.Model {
    private Gson mGson;
    private Application mApplication;

    @Inject
    public TakeCamera2Model(IRepositoryManager repositoryManager, Gson gson, Application application) {
        super(repositoryManager);
        this.mGson = gson;
        this.mApplication = application;
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mGson = null;
        this.mApplication = null;
    }

}